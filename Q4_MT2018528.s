		MOV R5, #16
		CMP R0, #11
		ITTE EQ
		MOVEQ R4, #1
		BEQ LOOP1
		BNE STOP		
		B STOP		

LOOP1	CMP R5, #30
		ITE EQ
		MOVEQ R6, #1
		MOVNE R7, #1
		
STOP    B STOP			;LOOP TO STOP THE EXECUTION
		ENDFUNC
		END
			
